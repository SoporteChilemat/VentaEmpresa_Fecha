package Clases;

public class Flete {
    
    String fechaFa;
    String numeroFa;
    String vendedor;
    String montoFa;
    String montoFaVendedor;
    String observacion;

    public String getFechaFa() {
        return fechaFa;
    }

    public void setFechaFa(String fechaFa) {
        this.fechaFa = fechaFa;
    }

    public String getNumeroFa() {
        return numeroFa;
    }

    public void setNumeroFa(String numeroFa) {
        this.numeroFa = numeroFa;
    }

    public String getVendedor() {
        return vendedor;
    }

    public void setVendedor(String vendedor) {
        this.vendedor = vendedor;
    }

    public String getMontoFa() {
        return montoFa;
    }

    public void setMontoFa(String montoFa) {
        this.montoFa = montoFa;
    }

    public String getMontoFaVendedor() {
        return montoFaVendedor;
    }

    public void setMontoFaVendedor(String montoFaVendedor) {
        this.montoFaVendedor = montoFaVendedor;
    }

    public String getObservacion() {
        return observacion;
    }

    public void setObservacion(String observacion) {
        this.observacion = observacion;
    }
}
