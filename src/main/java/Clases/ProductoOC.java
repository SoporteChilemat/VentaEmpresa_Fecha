public class ProductoOC {

    String codigo;
    String descripcion;
    String unidad;
    String cantidad;
    String precioFinal;
    String costoUnitario;
    String numeroOC;
    String valorFlete;

    public String getValorFlete() {
        return valorFlete;
    }

    public void setValorFlete(String valorFlete) {
        this.valorFlete = valorFlete;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getUnidad() {
        return unidad;
    }

    public void setUnidad(String unidad) {
        this.unidad = unidad;
    }

    public String getCantidad() {
        return cantidad;
    }

    public void setCantidad(String cantidad) {
        this.cantidad = cantidad;
    }

    public String getPrecioFinal() {
        return precioFinal;
    }

    public void setPrecioFinal(String precioFinal) {
        this.precioFinal = precioFinal;
    }

    public String getCostoUnitario() {
        return costoUnitario;
    }

    public void setCostoUnitario(String costoUnitario) {
        this.costoUnitario = costoUnitario;
    }

    public String getNumeroOC() {
        return numeroOC;
    }

    public void setNumeroOC(String numeroOC) {
        this.numeroOC = numeroOC;
    }
}
